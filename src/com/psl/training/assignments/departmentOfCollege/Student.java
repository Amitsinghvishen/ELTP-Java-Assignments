package com.psl.training.assignments.departmentOfCollege;

public class Student extends Person {
	private int rollno;
	private int fees;
	public int getRollno() {
		return rollno;
	}
	public void setRollno(int rollno) {
		this.rollno = rollno;
	}
	public int getFees() {
		return fees;
	}
	public void setFees(int fees) {
		this.fees = fees;
	}
	
}
